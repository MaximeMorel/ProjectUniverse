////////////////////////////////////////////////////////////////////////////////
#include "physicsBullet.hpp"
////////////////////////////////////////////////////////////////////////////////
PluginInfo pluginInfo = { "physicsBullet",
                          "physicsBullet",
                          0,
                          1};
////////////////////////////////////////////////////////////////////////////////
PluginPhysicsBullet* lib = nullptr;
////////////////////////////////////////////////////////////////////////////////
const PluginInfo* getPluginInfo()
{
    return &pluginInfo;
}
////////////////////////////////////////////////////////////////////////////////
Library* getLibInstance(Engine* engine)
{
    if (lib == nullptr)
    {
        lib = new PluginPhysicsBullet(*engine);
    }
    return lib;
}
////////////////////////////////////////////////////////////////////////////////
void closeLibInstance()
{
    delete lib;
    lib = nullptr;
}
////////////////////////////////////////////////////////////////////////////////
PluginPhysicsBullet::PluginPhysicsBullet(Engine &engine)
    : Library(engine)
{
}

void InputSDLDeviceKeyboard::update()
{
    super::update();

    SDL_Event event;

    while(SDL_PollEvent(&event))
    {
        switch(event.type)
        {
        case SDL_KEYDOWN:
            break;
        case SDL_KEYUP:
            break;
        }
    }
}

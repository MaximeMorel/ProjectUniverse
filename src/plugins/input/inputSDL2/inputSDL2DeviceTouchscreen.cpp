void InputSDL2DeviceTouchscreen::update()
{
    super::update();
}
////////////////////////////////////////////////////////////////////////////////
bool InputSDL2DeviceTouchscreen::update(SDL_Event* event)
{
    switch (event->type)
    {
    case SDL_FINGERDOWN:
        return true;
    case SDL_FINGERUP:
        return true;
    case SDL_FINGERMOTION:
        return true;
    }
    return false;
}

////////////////////////////////////////////////////////////////////////////////
#include "fboGL.hpp"
#include <GL/glew.h>
////////////////////////////////////////////////////////////////////////////////
FBOGL::FBOGL()
    : m_fboId(0)
{
}
////////////////////////////////////////////////////////////////////////////////
FBOGL::~FBOGL()
{
    glDeleteFramebuffers(1, &m_fboId);
}
////////////////////////////////////////////////////////////////////////////////
bool FBOGL::init()
{
    if (m_fboId == 0)
    {
        glGenFramebuffers(1, &m_fboId);
    }

    if (m_fboId == 0)
    {
        return false;
    }

    return true;
}
////////////////////////////////////////////////////////////////////////////////
GLuint FBOGL::getFboId() const
{
    return m_fboId;
}
////////////////////////////////////////////////////////////////////////////////
void FBOGL::printOn(Logger& o) const
{
    o << "FBOGL " << m_fboId;
}

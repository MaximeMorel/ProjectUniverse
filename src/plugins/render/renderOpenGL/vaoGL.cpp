////////////////////////////////////////////////////////////////////////////////
#include "vaoGL.hpp"
#include "core/log/logManager.hpp"
#include <GL/glew.h>
////////////////////////////////////////////////////////////////////////////////
VAOGL::VAOGL()
    : m_vaoId(0)
{
}
////////////////////////////////////////////////////////////////////////////////
VAOGL::~VAOGL()
{
    glDeleteVertexArrays(1, &m_vaoId);
}
////////////////////////////////////////////////////////////////////////////////
bool VAOGL::init()
{
    if (m_vaoId == 0)
    {
        glGenVertexArrays(1, &m_vaoId);
    }

    if (m_vaoId == 0)
    {
        return false;
    }

    return true;
}
////////////////////////////////////////////////////////////////////////////////
void VAOGL::bind()
{
    glBindVertexArray(m_vaoId);
}
////////////////////////////////////////////////////////////////////////////////
void VAOGL::printOn(Logger& o) const
{
    o << "VAOGL " << m_vaoId;
}

ResourceType GPUMeshGL4::type("GPUMeshGL4");
////////////////////////////////////////////////////////////////////////////////
GPUMeshGL4::GPUMeshGL4(const std::string& name)
: GPUMesh(name)
{
}
////////////////////////////////////////////////////////////////////////////////
GPUMeshGL4::~GPUMeshGL4()
{
    if (isEnginemanaged())
    {
        log().log() << __FUNCTION__ << ": " << *this << std::endl;
        res().delResource(this);
    }
}
////////////////////////////////////////////////////////////////////////////////
GPUMeshPtr GPUMeshGL4::create(const std::string& name)
{
    return std::make_shared<GPUMeshGL4>(name);
}

////////////////////////////////////////////////////////////////////////////////
#include "renderMeshGL33.hpp"
////////////////////////////////////////////////////////////////////////////////
RenderMeshGL33::RenderMeshGL33(const std::string& name)
    : RenderMeshGL(name)
{
}
////////////////////////////////////////////////////////////////////////////////
RenderMeshGL33::~RenderMeshGL33()
{
}
////////////////////////////////////////////////////////////////////////////////
RenderMeshPtr RenderMeshGL33::create(const std::string& name)
{
    return std::make_shared<RenderMeshGL33>(name);
}
////////////////////////////////////////////////////////////////////////////////
void RenderMeshGL33::setup(Mesh *mesh)
{
    vao.init();
    vao.bind();

    RenderMeshGL::setup(mesh);
}
////////////////////////////////////////////////////////////////////////////////
void RenderMeshGL33::draw()
{
    vao.bind();
    glDrawElements(GL_TRIANGLES, m_count, m_indicesType, nullptr);
}
////////////////////////////////////////////////////////////////////////////////
void RenderMeshGL33::printOn(Logger& o) const
{
    o << "RenderMeshGL33 " << getName();
}

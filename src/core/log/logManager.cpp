////////////////////////////////////////////////////////////////////////////////
#include "logManager.hpp"
#include <iostream>
////////////////////////////////////////////////////////////////////////////////
LogManager::LogManager()
{
    m_loggers.push_back(new LoggerStream("stdout", std::cout));
}
////////////////////////////////////////////////////////////////////////////////
LogManager::~LogManager()
{
    for (auto* logger : m_loggers)
    {
        delete logger;
    }
}
////////////////////////////////////////////////////////////////////////////////
Logger& LogManager::log()
{
    return *m_loggers[0];
}

#include <gtest/gtest.h>
#include "core/math/vec2.hpp"
////////////////////////////////////////////////////////////////////////////////
TEST(Vec2, ctor)
{
	Vec2 v;
	v.x = 0.0f;
	v.y = 0.0f;
	EXPECT_EQ(0.0f, v.x);
	EXPECT_EQ(0.0f, v.y);
}	
////////////////////////////////////////////////////////////////////////////////
int runTests(int argc, char** argv)
{
	::testing::InitGoogleTest(&argc, argv);
	int res = RUN_ALL_TESTS();

	return res;
}

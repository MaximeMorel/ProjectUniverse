////////////////////////////////////////////////////////////////////////////////
#include "fbo.hpp"
#include <GL/glew.h>
////////////////////////////////////////////////////////////////////////////////
FBOGL4::FBOGL4(const std::string& name)
: FBO(name)
{
    glGenFramebuffers(1, &m_fboId);
}
////////////////////////////////////////////////////////////////////////////////
FBOGL4::~FBOGL4()
{
    glDeleteFramebuffers(1, &m_fboId);
}

////////////////////////////////////////////////////////////////////////////////
#include "bufferObject.hpp"
#include <GL/glew.h>
////////////////////////////////////////////////////////////////////////////////
BufferObjectGL4::BufferObjectGL4(const std::string& name)
: BufferObject(name)
{
    glGenBuffers(1, &m_bufferId);
}
////////////////////////////////////////////////////////////////////////////////
BufferObjectGL4::~BufferObjectGL4()
{
    glDeleteBuffers(1, &m_bufferId);
}

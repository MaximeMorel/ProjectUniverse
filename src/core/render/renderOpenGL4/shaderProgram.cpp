////////////////////////////////////////////////////////////////////////////////
#include "shaderProgram.hpp"
#include <GL/glew.h>
////////////////////////////////////////////////////////////////////////////////
ShaderProgramGL4::ShaderProgramGL4(const std::string& name)
: ShaderProgram(name)
{
    m_shaderProgId = glCreateProgram();
}
////////////////////////////////////////////////////////////////////////////////
ShaderProgramGL4::~ShaderProgramGL4()
{
    glDeleteProgram(m_shaderProgId);
}

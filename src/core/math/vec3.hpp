#ifndef __VEC3_HPP__
#define __VEC3_HPP__
////////////////////////////////////////////////////////////////////////////////
#include <cstddef>
////////////////////////////////////////////////////////////////////////////////
template <typename T>
class TVec3
{
public:
    TVec3();
    TVec3(const T& x, const T& y, const T& z);
    
    size_t getMemSize() const;

    T x, y, z;
};
////////////////////////////////////////////////////////////////////////////////
typedef TVec3<int> Vec3i;
typedef TVec3<float> Vec3;
typedef TVec3<double> Vec3d;
////////////////////////////////////////////////////////////////////////////////
#endif // __VEC3_HPP__

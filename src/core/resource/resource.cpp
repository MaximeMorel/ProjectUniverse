void Resource::printOn(Logger& o) const
{

}
////////////////////////////////////////////////////////////////////////////////
Logger& operator<<(Logger& o, const Resource& res)
{
    res.printOn(o);
    return o;
}
////////////////////////////////////////////////////////////////////////////////
Logger& operator<<(Logger& o, ResourcePtr res)
{
    if (res)
        res->printOn(o);
    return o;
}

////////////////////////////////////////////////////////////////////////////////
#include "pluginApp.hpp"
////////////////////////////////////////////////////////////////////////////////
PluginApp::PluginApp(const std::string& filename)
    : Plugin(filename)
{
    if (m_handle != nullptr)
    {
        getAppInstance = (PFNgetAppInstance)(getSymbol("getAppInstance"));
        closeAppInstance = (PFNcloseAppInstance)(getSymbol("closeAppInstance"));
    }
}
////////////////////////////////////////////////////////////////////////////////
PluginApp::~PluginApp()
{
    if (closeAppInstance)
    {
        closeAppInstance();
    }
}

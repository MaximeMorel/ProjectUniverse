PluginLib::PluginLib(const std::string& filename)
    : Plugin(filename)
{
    if (m_handle != nullptr)
    {
        //getLibInstance = (PFNgetLibInstance)(getSymbol("getLibInstance"));
        closeLibInstance = (void (*)())(getSymbol("closeLibInstance"));
    }
}
////////////////////////////////////////////////////////////////////////////////
PluginLib::~PluginLib()
{
    if (closeLibInstance)
    {
        closeLibInstance();
    }
}
